    private void loadMenu() {

        adapter = new FirebaseRecyclerAdapter<Category, MenuViewHolder>(Category.class, R.layout.menu_item, MenuViewHolder.class, categories) {
            @Override
            protected void populateViewHolder(MenuViewHolder viewHolder, Category model, int position) {

                viewHolder.txtMenuName.setText(model.getName());
                Picasso.with(HomeActivity.this).load(model.getImage()).into(viewHolder.imageView);

                viewHolder.setItemClickListener(new ItemClickListener() {
                    @Override
                    public void onClick(View view, int position, boolean isLongClick) {
                        //Code late
                    }
                });

                viewHolder.setItemClickListener(new ItemClickListener() {
                    @Override
                    public void onClick(View view, int position, boolean isLongClick) {

                        //Get Category Id and Send to new Activity
                        Intent plantServiceList = new Intent(HomeActivity.this, PlantServiceListActivity.class);
                        //Because category is key, so we get key of this item
                        plantServiceList.putExtra("CategoryID", adapter.getRef(position).getKey());
                        startActivity(plantServiceList);

                    }
                });
            }
        };
        adapter.notifyDataSetChanged(); //Refresh data if have data changed
        recycler_menu.setAdapter(adapter);
    }

